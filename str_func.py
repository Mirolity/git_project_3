def to_uppercase(s):
    """
    Принимает строку и возвращает её со всеми заглавными буквами.

    Args:
        s (str): Входная строка.

    Returns:
        str: Строка в верхнем регистре.
    """
    return s.upper()


def capitalize_words(s):
    """
    Принимает строку и возвращает её с заглавными первыми буквами каждого слова.

    Args:
        s (str): Входная строка.

    Returns:
        str: Строка с заглавными первыми буквами каждого слова
    """
    return s.title()
